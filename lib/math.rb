def addition(num1, num2)
  sum = num1+num2
  sum
end

def subtraction(num1, num2)
  sum = num1 - num2
  sum
end

def division(num1, num2)
  sum = num1/num2
  sum
end

def multiplication(num1, num2)
  sum = num1 * num2
  sum
end

def modulo(num1, num2)
  remainder = num1 % num2
  remainder
end

def square_root(num)
  sum = Math.sqrt(num)
  sum
end

def order_of_operation(num1, num2, num3, num4)
  sum = num1 + ((num2 * num3) / num4)
  sum
